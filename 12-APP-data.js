// Helper: find context by mentee name (fuzzy match)
function getMenteeContext(nome) {
  if (!nome) return null;
  const n = nome.trim();
  // Exact match
  if (MENTEE_CONTEXTS[n]) return MENTEE_CONTEXTS[n];
  // Normalize: remove accents and lowercase
  const norm = (s) => s.normalize('NFD').replace(/[\u0300-\u036f]/g, '').toLowerCase();
  const nNorm = norm(n);
  for (const key of Object.keys(MENTEE_CONTEXTS)) {
    if (norm(key) === nNorm) return MENTEE_CONTEXTS[key];
  }
  // Fuzzy: first name + any shared token
  const nTokens = nNorm.split(/\s+/).filter(t => t.length > 2);
  for (const key of Object.keys(MENTEE_CONTEXTS)) {
    const kTokens = norm(key).split(/\s+/).filter(t => t.length > 2);
    if (nTokens[0] === kTokens[0] && nTokens.some(t => kTokens.includes(t))) return MENTEE_CONTEXTS[key];
  }
  return null;
}

